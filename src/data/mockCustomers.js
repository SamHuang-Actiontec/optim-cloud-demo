// ── PII Masking Helpers ──────────────────────────────────────────────────────
export function maskPhone(phone) {
  if (!phone) return null
  // Show only last 4 digits: (***) ***-8742
  const digits = phone.replace(/\D/g, '')
  const last4 = digits.slice(-4)
  return `(***) ***-${last4}`
}

export function maskAccount(account) {
  if (!account) return null
  // Show last 4: ****-****-1234
  const parts = account.split('-')
  const last = parts[parts.length - 1]
  return `****-****-${last}`
}

export function truncateNetworkId(networkId) {
  if (!networkId) return '—'
  // If it looks like a UUID/hash (no spaces, all hex + dashes, >16 chars), truncate
  if (/^[a-f0-9]{16,}$/i.test(networkId)) {
    return networkId.slice(0, 8) + '…'
  }
  return networkId.length > 24 ? networkId.slice(0, 22) + '…' : networkId
}

export function timeAgo(isoString) {
  if (!isoString) return 'Unknown'
  const diff = Date.now() - new Date(isoString).getTime()
  const s = Math.floor(diff / 1000)
  if (s < 60) return `${s}s ago`
  const m = Math.floor(s / 60)
  if (m < 60) return `${m}m ago`
  const h = Math.floor(m / 60)
  if (h < 24) return `${h}h ago`
  const d = Math.floor(h / 24)
  return `${d}d ago`
}
